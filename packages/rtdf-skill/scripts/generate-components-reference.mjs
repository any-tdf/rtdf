import { execFileSync } from 'node:child_process';
import { existsSync, mkdirSync, readFileSync, rmSync, writeFileSync } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const packageRoot = path.resolve(scriptDir, '..');
const rtdfRoot = path.resolve(packageRoot, '../..');
const anyRoot = path.resolve(rtdfRoot, '..');

const generateComponentDocs = () => {
	const commonRoot = path.join(anyRoot, 'site-common');
	const generator = path.join(commonRoot, 'scripts/generate-component-docs.mjs');
	if (!existsSync(generator)) {
		console.warn('Skipped component docs generation because site-common is unavailable.');
		return;
	}

	execFileSync('bun', ['run', 'scripts/generate-component-docs.mjs', '--target', 'rtdf'], {
		cwd: commonRoot,
		stdio: 'inherit'
	});
};

const parseMenuList = () => {
	const sourcePath = path.join(anyRoot, 'site-common/src/stdf-data/menuList.ts');
	const source = readFileSync(sourcePath, 'utf-8');
	const marker = 'export const menuList: MenuList[] = ';
	const start = source.indexOf(marker);
	const arrayStart = source.indexOf('[', start);
	const arrayEnd = source.indexOf('\n];', arrayStart) + 2;
	const arrayText = source.slice(arrayStart, arrayEnd);
	return new Function(`return ${arrayText};`)();
};

const listDocs = (componentName) => {
	const docsDir = path.join(rtdfRoot, 'docs/mds/components', componentName);
	if (!existsSync(docsDir)) {
		return [];
	}
	const preferred = ['guide_en.md', 'api_en.md', 'FAQ_en.md', 'version_en.md'];
	return preferred.filter((item) => existsSync(path.join(docsDir, item))).map((item) => path.join(docsDir, item));
};

const normalizeDoc = (content) =>
	content
		.replace(
			'[德育处主任](https://gitee.com/k21vin/front-end-data-visualization/tree/master/src/views/Native/PureCSS/pages/Loading/components)',
			'[front-end-data-visualization](https://gitee.com/k21vin/front-end-data-visualization/tree/master/src/views/Native/PureCSS/pages/Loading/components)'
		)
		.replaceAll('STDF-style', 'STDF-compatible')
		.replaceAll('compatible with STDF', 'compatible with STDF migration')
		.trim();

const readDoc = (docPath) => normalizeDoc(readFileSync(docPath, 'utf-8'));

const formatDocTitle = (docPath) => path.basename(docPath).replace('_en.md', '').replace('.md', '');

const renderComponentDetail = (row) => {
	const docSections = row.docs
		.map((docPath) => {
			const title = formatDocTitle(docPath);
			return `## ${title}\n\n${readDoc(docPath)}`;
		})
		.join('\n\n');

	return `# ${row.component}

<!-- generated by rtdf-skill component reference generator -->

- Category: ${row.category}
- Route nav: \`${row.nav}\`

## Offline Documentation

This file embeds the English component guide, API, FAQ, and version documentation. Treat the content below as the bundled documentation source for ordinary RTDF projects that only install the npm \`rtdf\` package.

${docSections || '## Docs\n\nNo component docs found.'}
`;
};

generateComponentDocs();

const menuList = parseMenuList();
const rows = menuList.flatMap((group) =>
	group.childs.map((child) => ({
		category: group.class_en,
		component: child.title_en,
		nav: child.nav,
		docs: listDocs(child.nav)
	}))
);

const componentReferenceDir = path.join(packageRoot, 'skill/references/components');
rmSync(componentReferenceDir, { recursive: true, force: true });
mkdirSync(componentReferenceDir, { recursive: true });

rows.forEach((row) => {
	writeFileSync(path.join(componentReferenceDir, `${row.nav}.md`), renderComponentDetail(row), 'utf-8');
});

const output = `# RTDF Component Reference

<!-- generated by rtdf-skill component reference generator -->
<!-- component-count: ${rows.length} -->

Use this reference when an AI task needs to select RTDF components, locate documentation, or inspect implementation details. Do not guess component APIs from the component name. Open the generated component detail file first. The detail files are bundled in this Skill package for ordinary user projects that only install the npm \`rtdf\` package and do not have RTDF repository docs locally.

## Lookup Rules

- Generated component detail files live under \`references/components/<nav>.md\` inside this Skill package.
- Use the generated detail files as the offline component documentation source.
- Do not ask ordinary user projects to provide RTDF repository docs or component source files.
- When a generated component detail file is loaded, trust the embedded guide, API, FAQ, and version sections for that component.

## Component Index

| Category | Component | Route nav | Offline Skill docs |
| --- | --- | --- | --- |
${rows.map((row) => `| ${row.category} | ${row.component} | \`${row.nav}\` | \`references/components/${row.nav}.md\` |`).join('\n')}
`;

writeFileSync(path.join(packageRoot, 'skill/references/components.md'), output, 'utf-8');
console.log(`Generated ${rows.length} component entries and detail files.`);
